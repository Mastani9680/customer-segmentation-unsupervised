# evaluation

import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.metrics import silhouette_score, davies_bouldin_score
from sklearn.decomposition import PCA

# 1. Cluster Distribution
def show_cluster_distribution(rfm):
    plt.figure(figsize=(6,4))
    sns.countplot(x='Cluster', data=rfm)
    plt.title("Customer Cluster Distribution")
    plt.show()


# 2. Cluster Summary
def show_cluster_summary(rfm):
    summary = rfm.groupby('Cluster')[['Recency','Frequency','Monetary']].mean()
    print("\nCluster Summary:\n", summary)



# 3. Clustering Metrics
def show_metrics(X_scaled, rfm):
    labels = rfm['Cluster']

    if len(set(labels)) > 1:
        sil = silhouette_score(X_scaled, labels)
        db = davies_bouldin_score(X_scaled, labels)

        print("\nSilhouette Score:", round(sil, 3))
        print("Davies-Bouldin Score:", round(db, 3))
    else:
        print("Not enough clusters to evaluate")

# 4. PCA Visualization
def show_pca(X_scaled, rfm):
    pca = PCA(n_components=2)
    X_pca = pca.fit_transform(X_scaled)

    plt.figure(figsize=(6,4))
    plt.scatter(X_pca[:,0], X_pca[:,1], c=rfm['Cluster'])
    plt.title("Clusters Visualization (PCA)")
    plt.xlabel("PCA 1")
    plt.ylabel("PCA 2")
    plt.show()

def evaluate_model(rfm, X_scaled):
    show_cluster_distribution(rfm)
    show_cluster_summary(rfm)
    show_metrics(X_scaled, rfm)
    show_pca(X_scaled, rfm)

evaluate_model(rfm, X_scaled)